def divide(a, b):
    print(f"Dividing {a} / {b}")
    return a / b

def multiply(a, b):
    print(f"MULTIPLYING {a} * {b}")
    return a * b

def subtract(a, b):
    print(f"SUBTRACTING {a} - {b}")
    return a - b

